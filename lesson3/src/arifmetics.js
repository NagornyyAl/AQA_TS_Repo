const firstNumber = 18;
const secondNumber = 4.3;

const firstString = '25';
const secondString = '7';

const firstBoolean = true;
const secondBoolean = false;

console.log('Arithmetic operations with the same types:');
console.log('firstNumber + secondNumber =', firstNumber + secondNumber);
console.log('firstNumber - secondNumber =', firstNumber - secondNumber);
console.log('firstNumber * secondNumber =', firstNumber * secondNumber);
console.log('firstNumber / secondNumber =', firstNumber / secondNumber);
console.log('firstNumber % secondNumber =', firstNumber % secondNumber);
console.log('firstString + secondString =', firstString + secondString);
console.log('firstBoolean + secondBoolean =', firstBoolean + secondBoolean);

console.log('\nArithmetic operations with different types:');
console.log('firstNumber + firstString =', firstNumber + firstString);
console.log('firstNumber - firstString =', firstNumber - firstString);
console.log('firstNumber * firstBoolean =', firstNumber * firstBoolean);
console.log('secondNumber + secondBoolean =', secondNumber + secondBoolean);
console.log('firstString / secondNumber =', firstString / secondNumber);
